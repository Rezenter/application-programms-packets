% --- Executes on selection change in popupmenu1.
function popupmenu1_Callback(hObject, eventdata, handles)
popup_sel_index = get(handles.popupmenu1, 'Value');
xmin = -10;
ymin = -10;
xmax = 10;
ymax = 10;
tlim = [0 100];
cond = false;
switch popup_sel_index
    case 1 
        cond = false;
        %do nothing
    case 2
        cond = true;
        a = 1
        b = 2
    case 3
        cond = true;
        a = 1
        b = 1
    case 4
        cond = true;
        a = 1
        b = 3
end
while cond;
            figure(1), hold on
            axis([xmin xmax ymin ymax]);
            title('phase portrait');
            start = ginput(1);
            [t, c] = ode23t( @(t, c) model(t, c, a, b), tlim, start);
            comet(c(:,1),c(:,2))
            plot(c(:,1),c(:,2)) 
            figure(2), hold on 
            plot(t,c(:,1),t,c(:,2))
            title('time portrait');
end
